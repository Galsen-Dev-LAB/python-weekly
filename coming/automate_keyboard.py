import pyautogui
# pip install pyautogui

pyautogui.typewrite('Hello World!')
# prints out 'Hello World' instantly

pyautogui.typewrite('Hello World!', interval=0.25)

# Prints out 'Hello World' with a quarter
# second delay after each character

# Automate keyboard Using Python
# pyautogui is a python module used to automate stuff like mouse keyboard action.